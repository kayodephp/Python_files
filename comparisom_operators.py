# Comparison operators are used to compare 2 values
# They either return true or false according to a condition

# ==
print(7 == 8) # This returns False
print('abc' == 'abc') # This returns True

# !=
print(7 != 7)
print('a' != 'A')

# >
a, b = 7, 9
print(a > b)

# >=
print(b >= 9)

# <
print(a < 8)

# <=
print(a * 2 <= 100)